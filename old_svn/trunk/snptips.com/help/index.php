						<h1>Help</h1>
						
						<h3>Getting Started with SNPTips</h3>
						
						<p>Before you can use SNPTips, you will need to download your 23andMe or deCODEme raw data, if you have not already done so. SNPTips requires you to have your raw data on your local machine so we don't need to log in to your 23andMe or deCODEme account, and so we can search the 600,000+ SNPs quickly.</p>
						
						<h3>23andMe Customers: how to download your raw data:</h3>
						
						<ol>
							<li>Log in to your 23andMe.com account.</li>
							<li>Choose "Browse Raw Data" from the Account dropdown in the upper right part of the 23andMe page.</li>
							<li>Click the "download raw data" link in the upper right of the Browse Raw Data page.</li>
							<li>Follow the instructions on this page, and be sure "All DNA" is selected in the "Data set" dropdown.</li>
							<li>Click the Download Data button to initiate the download.</li>
							<li>The file is ~14MB and may take a while to download, depending on your connection speed.</li>
							<li>The file will download as a ZIP-formatted file &ndash; double-click it to unzip it.</li>
							<li>Note the location of the unzipped text (.txt) file - this is the file you need for SNPTips - move it to a desired permanent location before using it with SNPTips, since you will have to point SNPTips to its location.</li>
						</ol> 
						
						<p class="callout">
							<em><strong>NOTE</strong>: your raw data is your complete 23andMe SNP profile, and you should take care to safeguard it &ndash; don't download it to a machine you don't control, etc.  If this makes you uncomfortable, SNPTips may not be for you.</em>
						</p>
						
						
						<h3>deCODEme Customers: how to download your raw data: </h3>

						<ol>
							<li>Go to <a href="https://www.decodeme.com/download" target="_blank">https://www.decodeme.com/download</a></li>
							<li>Log in to your deCODEme account.</li>
							<li>Enter your password again and click the Download button.</li>
							<li>The file is ~10MB and may take a while to download, depending on your connection speed.</li>
							<li>The file will download as a ZIP-formatted file &ndash; double-click it to unzip it.  It will create a folder containing your SNP data &ndash; move it to a desired permanent location before using it with SNPTips, since you will have to point SNPTips to its location.</li>
							<li>Navigate into the folder and note the location of the unzipped CSV (.csv) file - this is the file you need for SNPTips.</li>
						</ol>
						
						<p class="callout">
							<em><strong>NOTE</strong>: your raw data is your complete deCODEme SNP profile, and you should take care to safeguard it &ndash; don't download it to a machine you don't control, etc.  If this makes you uncomfortable, SNPTips may not be for you.</em>
						</p>
						
						<h3>Configuring SNPTips</h3>
						
						<p>Before you can enable SNPTips, you must point it to your 23andMe raw data (.txt) file or deCODEme raw data (.csv) file - see above for instructions on how to obtain your raw data file.</p>
						
						<ol>
							<li>From either the Firefox Tools > SNPTips menu, or the SNPTips menu accessed by clicking the SNPTips status bar icon, select Preferences.</li>
							<li>Click the Browse... button next to the Data File: field, and browse to the location of your 23andMe raw data (.txt) file or deCODEme raw data (.csv) file. For 23andMe data, the filename will start with genome_<your name> and end with a .txt extension.  For deCODEme data, the filename will be deCODEme_scan.csv.</li>
							<li>Check the "Enable SNPTips" checkbox to turn on SNPTips.</li>
							<li>Click the OK button to save changes.</li>
						</ol>
						
						<p>You&#8217;re now ready to browse SNPs! To try it out, reload this page and look at the text below &ndash; you should see a green box around the SNP RSID:</p>
						
						<p class="callout">
							Click on the icon next to rs17822931 to see if your earwax is wet (CC or CT).  TT&#8217;s at rs17822931 have dry earwax!
						</p>
						
						<p>To give it a real-world test, go to <a href="http://spittoon.23andme.com/category/snpwatch/" target="_blank">the SNPWatch section of The Spittoon</a> (23andMe's blog) and select any article - these articles generally mention various SNPs by RSID, and describe the implications of different alleles (versions).  You'll be able to see your alleles at a glance!</p>  
		  			
